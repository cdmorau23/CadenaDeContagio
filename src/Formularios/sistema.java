    public void insertarDatos(){
        try{
        String SQL= "INSERT INTO `persona` (`CC`, `Nombre`, `domicilio`, `telefono`, `correo_electronico`, `fecha_nacimiento`, `genero`) VALUES (?,?,?,?,?,?,?);";
        PreparedStatement pst =(PreparedStatement) con.prepareStatement(SQL);
        pst.setString( 1, txtCC.getText());
        pst.setString( 2, txtNombre.getText());
        pst.setString( 3, txtDomicilio.getText());
        pst.setString( 4, txtTelefono.getText());
        pst.setString( 5, txtCorreo.getText());
        pst.setString( 6, txtFechaNacimiento.getText());
        int seleccionado= txtGenero.getSelectedIndex();
        pst.setString( 7, txtGenero.getItemAt(seleccionado));
       
        pst.execute();
        JOptionPane.showMessageDialog(null, "RegistroExitoso");   
        }
        catch(Exception e){
        JOptionPane.showMessageDialog(null, "R No Exitoso"+e.getMessage());

        }
    }
    
    public void ActualizarDatos(){
        try{
        String SQL= "UPDATE persona SET CC=?,Nombre=?,domicilio=?,telefono=?,correo_electronico=?,fecha_nacimiento=?,genero=? where CC=?;";
        
        int filaSeleccionada = tablaPersona.getSelectedRow();
        String dao = (String) tablaPersona.getValueAt(filaSeleccionada, 0);
        
        PreparedStatement pst =(PreparedStatement) con.prepareStatement(SQL);
        pst.setString( 1, txtCC.getText());
        pst.setString( 2, txtNombre.getText());
        pst.setString( 3, txtDomicilio.getText());
        pst.setString( 4, txtTelefono.getText());
        pst.setString( 5, txtCorreo.getText());
        pst.setString( 6, txtFechaNacimiento.getText());
        int seleccionado= txtGenero.getSelectedIndex();
        pst.setString( 7, txtGenero.getItemAt(seleccionado));
        
        pst.setString(8, dao);
        pst.execute();
        JOptionPane.showMessageDialog(null, "Registro Editado Exitosamente");   
        }
        catch(Exception e){
        JOptionPane.showMessageDialog(null, "R No editado de manera Exitosa"+e.getMessage());

        }
    }
    
    public void eliminarRegistros(){
        int filaSeleccionada = tablaPersona.getSelectedRow();
        try{
            String SQL = "delete from Persona where CC="+tablaPersona.getValueAt(filaSeleccionada,0);
            
            Statement st= (Statement) con.createStatement();
            
            int n = st.executeUpdate(SQL);
            
            if (n>=0){
                JOptionPane.showMessageDialog(null, "Registro eliminado");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error al eliminar registro"+ e.getMessage());

        }
    }
    
