    public void mostrarDatos(){
        String[] titulos = {"CC","Nombre","domicilio","telefono","correo_electronico","fecha_nacimiento","genero"};
        String[] registros = new String[7];
        
        DefaultTableModel modelo = new DefaultTableModel(null,titulos);
        String SQL = "SELECT * FROM PERSONA";
        
        try{
            Statement st = (Statement) con.createStatement();
            ResultSet rs = (ResultSet) st.executeQuery(SQL);
            while(rs.next()){
                
                registros[0] = rs.getString("CC");
                registros[1] = rs.getString("Nombre");
                registros[2] = rs.getString("domicilio");
                registros[3] = rs.getString("telefono");
                registros[4] = rs.getString("correo_electronico");
                registros[5] = rs.getString("fecha_nacimiento");
                registros[6] = rs.getString("genero");
                
                modelo.addRow(registros);
                                
            }
            tablaPersona.setModel(modelo);

        }
        catch(Exception e){
            
            JOptionPane.showMessageDialog(null, "No se pudo mostrar la lista" +e.getMessage());

            
        }
        
        
    }
